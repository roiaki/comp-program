process.stdin.resume();
process.stdin.setEncoding('utf8');
// 自分の得意な言語で
// Let's チャレンジ！！
var lines = [];
var reader = require('readline').createInterface({
  input: process.stdin,
  output: process.stdout
});
reader.on('line', (line) => {
  lines.push(line);
});
reader.on('close', () => {
  let pastYear = parseInt(lines[0]);
  let nextYear = parseInt(lines[1]);
  let ans = nextYear - pastYear;
  console.log(ans);
});